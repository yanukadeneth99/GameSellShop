    private DefaultCategoryDataset createDataset() {
        ordersList.clear();
        ordersList = ordersQuery.getResultList();

        //No need try pass. The caller function has a try catch on it
        int totyear = 0;
        for (Orders o : ordersList) {
            Date date = o.getPurchaseddate();
            Calendar cal = Calendar.getInstance(TimeZone.getTimeZone("Europe/Paris"));
            cal.setTime(date);
            int year = cal.get(Calendar.YEAR);
            
            if (year > totyear) {
                totyear = year;
            }
        }
        
        int count1, count2, count3, count4, count5, count6;
        count1 = count2 = count3 = count4 = count5 = count6 = 0;
        
        for (Orders o : ordersList) {
            Date date = o.getPurchaseddate();
            Calendar cal = Calendar.getInstance(TimeZone.getTimeZone("Europe/Paris"));
            cal.setTime(date);
            
            if (cal.get(Calendar.YEAR) == totyear) {
                count1++;
            } else if (cal.get(Calendar.YEAR) == (totyear - 1)) {
                count2++;
            } else if (cal.get(Calendar.YEAR) == (totyear - 2)) {
                count3++;
            } else if (cal.get(Calendar.YEAR) == (totyear - 3)) {
                count4++;
            } else if (cal.get(Calendar.YEAR) == (totyear - 4)) {
                count5++;
            } else if (cal.get(Calendar.YEAR) == (totyear - 5)) {
                count6++;
            }
        }
        
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.addValue(count1, "Date", "" + (totyear));
        dataset.addValue(count2, "Date", "" + (totyear - 1));
        dataset.addValue(count3, "Date", "" + (totyear - 2));
        dataset.addValue(count4, "Date", "" + (totyear - 3));
        dataset.addValue(count5, "Date", "" + (totyear - 4));
        dataset.addValue(count6, "Date", "" + (totyear - 5));
        
        return dataset;
    }

