    private void MakePieChart() {
        try {
            int newg, hotg, nag, ag;
            newg = hotg = nag = ag = 0;
            for (Games g : gamesList) {
                if (g.getStatus().getId() == 1) {
                    newg++;
                } else if (g.getStatus().getId() == 2) {
                    hotg++;
                } else if (g.getStatus().getId() == 3) {
                    nag++;
                } else if (g.getStatus().getId() == 4) {
                    ag++;
                }
            }

            //PieChart
            DefaultPieDataset dataset = new DefaultPieDataset();
            dataset.setValue("New Games", newg);
            dataset.setValue("Hot Games", hotg);
            dataset.setValue("Avaliable", ag);
            dataset.setValue("Not Avaliable", nag);
            
            JFreeChart chart = ChartFactory.createPieChart3D("Games Status Pie Chart", dataset, true, true, false);
            final PiePlot3D plot = (PiePlot3D) chart.getPlot();
            plot.setStartAngle(270);
            plot.setForegroundAlpha(0.6f);
            plot.setInteriorGap(0.02);
            
            ChartPanel chartPanel = new ChartPanel(chart);
            pnChart.removeAll();
            pnChart.add(chartPanel, BorderLayout.CENTER);
            pnChart.validate();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Failed to Create Pie Chart. Please try again later.", "Error : Failed to Load Pie Chart", JOptionPane.ERROR_MESSAGE);
            logr.log(Level.SEVERE, "Error : Failed to Load Pie Chart : " + ex.getMessage());
        }
    }
    
